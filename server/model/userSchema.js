const mongoose = require("mongoose");

// delcaring user schema
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  work: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  cpassword: {
    type: String,
    required: true,
  },
});

// Creating collection
// const User = mongoose.model('collectionName', schema);

const User = mongoose.model("USER", userSchema);

module.exports = User;
